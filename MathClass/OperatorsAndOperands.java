package MathClass;

public class OperatorsAndOperands {
    public static void main(String[] args) {
        double friends = 14;

        // operands = values, variables, numbers, quantity
        // operators = + - / %

        friends--;
        friends = friends % 2; // % : Rest of division
        System.out.println(friends);

        friends = friends / 3;
        System.out.println(friends);

    }
}
